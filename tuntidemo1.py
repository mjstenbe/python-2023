# Esimerkkitehtävä: kumpi on suurempi?

# Anna ensimmäinen luku: 20
luku1 = int(input("Anna ensimmäinen luku: "))

# Anna toinen luku: 14
luku2 = int(input("Anna toinen luku: "))

# Luvuista suurempi: 20
if luku1 > luku2:
    print("Luku 1 on suurempi kuin luku 2.")

elif luku2 > luku1:
    print("Luku 2 on suurempi kuin luku 1.")

else:
    print("Luvut ovat yhtäsuuria!.")
